package com.inetpsa.oaz00.ws.checker.formula.services;

import java.util.ArrayList;
import java.util.List;

/**
 * The Class EquationVariables.
 * 
 * @author U224106
 */
public class EquationVariables {

	/** The variables list. */
	private List<String> lVariables = new ArrayList<String>();

	/**
	 * Instantiates a new equation variables.
	 */
	public EquationVariables() {
	}

	/**
	 * Checks if it's a variable.
	 * 
	 * @param s
	 *            the s
	 * @return true, if is a variable
	 */
	public boolean isVariable(String s) {
		return lVariables.contains(s);
	}

	/**
	 * Adds the variable.
	 * 
	 * @param s
	 *            the variable to add
	 */
	public void addVariable(String s) {
		lVariables.add(s);
	}

	/**
	 * Gets the variables.
	 * 
	 * @return the variables
	 */
	public List<String> getVariables() {
		return lVariables;
	}
}
